def write_pair_class(name: str, pairs: list[tuple[str, str]], script: str | None = None) -> None:
    d = ROOT / name
    d.mkdir(parents=True, exist_ok=True)
    sources, refs, items = [], [], []
    for i, (src, tgt) in enumerate(pairs, start=1):
        sid = f"{name}-{i:03d}"
        row: dict = {"id": sid, "source": src, "status": "reviewed"}
        if script:
            row["script"] = script
        sources.append(row)
        refs.append({"id": sid, "reference": tgt})
        items.append({"id": sid, "status": "reviewed"})
    (d / "sources.jsonl").write_text(
        "\n".join(json.dumps(x, ensure_ascii=False) for x in sources) + "\n",
        encoding="utf-8",
    )
    (d / "references.jsonl").write_text(
        "\n".join(json.dumps(x, ensure_ascii=False) for x in refs) + "\n",
        encoding="utf-8",
    )
    (d / "manifest.json").write_text(
        json.dumps(
            {"class": name, "n_target": 100, "n_filled": 100, "items": items},
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )
    print(f"{name}: 100/100")


